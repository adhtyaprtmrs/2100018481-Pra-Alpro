public class Identitas {
    protected String usia;
    protected String nama;

    public Identitas() {
    }

    public void setUsia(String usia) {
        this.usia = usia;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }

    public String getUsia() {
        return usia;
    }
}
